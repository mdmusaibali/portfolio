const PROJECTS = [...OWN_PROJECTS, ...PRACTICE_PROJECTS];

const REACT_APP_SUPABASE_BUCKET_BASE_URL =
  "https://tmofcpkjbtgzcmzjgtgq.supabase.co/storage/v1/object/public/portfolio-assets/";
PROJECTS.forEach((project) => {
  if (project.icon && project.icon.includes("/")) {
    project.icon =
      REACT_APP_SUPABASE_BUCKET_BASE_URL +
      "project-icons/" +
      project.icon.split("/").pop();
  }
  if (Array.isArray(project.desktopImages)) {
    let supaURLArr = [];
    project.desktopImages.forEach((image) => {
      supaURLArr.push(
        REACT_APP_SUPABASE_BUCKET_BASE_URL +
          "project-images/" +
          image.split("/").pop()
      );
    });
    project.desktopImages = JSON.stringify(supaURLArr);
  }
  if (Array.isArray(project.mobileImages)) {
    let supaURLArr = [];
    project.mobileImages.forEach((image) => {
      supaURLArr.push(
        REACT_APP_SUPABASE_BUCKET_BASE_URL +
          "project-images/" +
          image.split("/").pop()
      );
    });
    project.mobileImages = JSON.stringify(supaURLArr);
  }
  if (project.date) {
    project.date = new Date(project.date).toISOString();
  }
  if (project.icon) {
    project.iconURL = project.icon;
    delete project.icon;
  }
  if (project.live) {
    project.liveURL = project.live;
    delete project.live;
  }
  delete project.id;
  delete project.no;
});
